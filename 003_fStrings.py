# Formatted Strings (f-Strings) in Python
# f-Strings, introduced in Python 3.6, provide a way to embed expressions inside string literals, using curly braces {}.
# They are prefixed with the letter 'f' or 'F'. This allows for more readable and concise string formatting.
firstName = "John"
age = 30
height = 5.9
is_student = True
# Using f-Strings to format the output
print(f"Name: {firstName}")
print(f"Age: {age}")
print(f"Height: {height}")
print(f"Is Student: {is_student}")