#  Variables -> A variable in Python is a named location used to store data in memory. You can create a variable by assigning a 
# value to it using the equals sign (=). Variables can hold different types of data, such as numbers, strings, lists, and more.
# Python is a dynamically typed language, so you don't need to declare the type of a variable explicitly; it is inferred from the value assigned at runtime.

firstName = "John"          # A string variable
age = 30                    # An integer variable
height = 5.9                # A float variable
is_student = True          # A boolean variable
print("Name:", firstName)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)